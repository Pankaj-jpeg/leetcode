// Approach: sorting using nth_element | Time: O(n log n) | Space: O(1)
// Time       : 
// Space      : 
// Runtime    : 0 ms  |  Memory: 69 MB
// Date       : 2026-07-07
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class Solution {
public:

    int findKthLargest(vector<int>& nums, int k) {
        int n = nums.size();
        nth_element(nums.begin(),nums.end()-k,nums.end());
        return nums[n-k];
    }
};