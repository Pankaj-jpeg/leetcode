// Date       : 2026-07-14
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class Solution {
public:
    int maxSubArray(vector<int>& nums) {
        int Max = INT_MIN;
        int sum = 0;
        for(int i = 0;i<nums.size();i++){
            sum+=nums[i];
            Max = max(Max,sum);
            if(sum<0)
                sum = 0;
        }
        return Max;
    }
};