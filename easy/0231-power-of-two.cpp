////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// Approach: bitwise operation, checking for zero in the least significant bit | Time: O(1) | Space: O(1)
// Time       : 
// Space      : 
// Runtime    : 0 ms  |  Memory: 8 MB
// Date       : 2026-07-18
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class Solution {
public:
    bool isPowerOfTwo(int n) {
        if(n<=0) return false;
        int  k = n - 1;
        return (k&n) == 0;
    }
};