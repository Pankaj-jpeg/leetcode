////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// Approach: bitwise manipulation, counting set bits | Time: O(log(n)) | Space: O(1))
// Time       : 
// Space      : 
// Runtime    : 0 ms  |  Memory: 8.3 MB
// Date       : 2026-07-18
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class Solution {
public:
    int hammingWeight(int n) {
        int cnt = 0;
        while(n>0){
            cnt+=(n & 1);
            n = (n >> 1); 
        }

        return cnt;
    }
};