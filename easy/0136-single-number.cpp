// 136. Single Number
// Difficulty : Easy
// Link       : https://leetcode.com/problems/single-number/
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// Approach: bitwise XOR operation | Time: O(n) | Space: O(1)
// Time       : 
// Space      : 
// Runtime    : 0 ms  |  Memory: 20.7 MB
// Date       : 2026-07-14
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class Solution {
public:
    int singleNumber(vector<int>& nums) {
        int Xor = 0;

        for(int i = 0;i<nums.size();i++){
            Xor = Xor ^ nums[i];
        }

        return Xor;
    }
};