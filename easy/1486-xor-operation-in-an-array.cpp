////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// Approach: simple iteration with XOR operation | Time: O(n) | Space: O(1)
// Time       : 
// Space      : 
// Runtime    : 0 ms  |  Memory: 8 MB
// Date       : 2026-06-23
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class Solution {
public:
    int xorOperation(int n, int start) {
        int res = 0;
        for(int i = 0;i<n;i++){
            res ^= (start + 2*i);
        }
        return res;
    }
};