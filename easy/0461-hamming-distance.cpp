////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// Approach: bitwise XOR and counting set bits | Time: O(1) | Space: O(1))
// Time       : 
// Space      : 
// Runtime    : 0 ms  |  Memory: 7.9 MB
// Date       : 2026-06-23
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class Solution {
public:
    int hammingDistance(int x, int y) {
        int counter = x ^ y;
        int cnt = 0;
        for(int i = 0;i<32;i++){
            if((counter & (1 << i))) cnt++;
        }

        return cnt;
    }
};