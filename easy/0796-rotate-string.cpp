////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// Approach: string concatenation and search | Time: O(n) | Space: O(n)
// Time       : 
// Space      : 
// Runtime    : 0 ms  |  Memory: 8 MB
// Date       : 2026-07-16
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class Solution {
public:
    bool rotateString(string s, string goal) {
        int m = s.size();
        int n = goal.size();

        if(m!=n)
            return false;
        
        return (s+s).find(goal)!=string::npos;
    }
};