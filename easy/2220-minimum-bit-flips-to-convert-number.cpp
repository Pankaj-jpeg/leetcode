////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// Approach: bitwise XOR and counting set bits | Time: O(log(max(start, goal))) | Space: O(1))
// Time       : 
// Space      : 
// Runtime    : 0 ms  |  Memory: 7.9 MB
// Date       : 2026-07-19
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

class Solution {
public:
    int minBitFlips(int start, int goal) {
        int k = (start ^ goal);
        int cnt = 0;
        while(k>0){
            cnt += (k&1);
            k = (k >> 1);
        }
        return cnt;
    }
};